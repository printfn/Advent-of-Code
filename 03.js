function count(x, y) {
    let trees = 0;
    for (let i in input) {
        if (i % y != 0) {
            continue;
        }
        let row = input[i];
        let cell = row[(i / y * x) % row.length];
        if (cell == '#') {
            trees++;
        }
    }
    return trees;
}

