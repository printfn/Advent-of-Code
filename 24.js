let tilesPositions = [];
let count = 0;

const size = 136;

for (let stepsStr of input) {
    let x = 0;
    let y = 0;
    let prev = '';
    let dir = '';
    let dirs = [];
    for (let ch of stepsStr) {
        if (prev == '') {
            if (ch == 'e') {
                dir = 'e';
            } else if (ch == 'w') {
                dir = 'w';
            } else {
                prev = ch;
            }
        } else {
            if (prev == 's' && ch == 'e') {
                dir = 'se';
            } else if (prev == 's' && ch == 'w') {
                dir = 'sw';
            } else if (prev == 'n' && ch == 'e') {
                dir = 'ne';
            } else if (prev == 'n' && ch == 'w') {
                dir = 'nw';
            } else {
                console.error('unknown direction', prev, ch);
            }
            prev = '';
        }
        if (dir != '') {
            dirs.push(dir);
            prev = '';
            dir = '';
        }
    }
    for (let dir of dirs) {
        let s = y % 2 == 0 ? 0 : 1;
        switch (dir) {
            case 'e' : ++x; break;
            case 'w' : --x; break;
            case 'ne': ++y; break;
            case 'sw': --y; break;
            case 'nw': ++y, --x; break;
            case 'se': --y, ++x; break;
        }
    }
    let xy = (size / 2 + x) * size + y + size / 2;
    if (tilesPositions.includes(xy)) {
        //console.log('reverting', xy);
        --count;
        let idx = tilesPositions.indexOf(xy);
        tilesPositions.splice(idx, 1);
    } else {
        //console.log('flipping', xy);
        tilesPositions.push(xy);
        ++count;
    }
}

let grid = new Uint8Array(size ** 2);
for (let pos of tilesPositions) {
    grid[pos] = 1;
    // 1 is black, 0 is white
}

let grid2 = new Uint8Array(size ** 2);

function next(arr, res) {
    for (let x = 1; x < size-1; ++x) {
        for (let y = 1; y < size-1; ++y) {
            let cur = arr[x * size + y];
            let neighbours = arr[(x+1) * size + y] + arr[(x-1) * size + y] + arr[x * size + y+1] + arr[x * size + y-1] + arr[(x-1) * size + y+1] + arr[(x+1) * size + y-1];
            if (cur == 1) {
                // black tile
                if (neighbours > 2 || neighbours == 0) {
                    res[x * size + y] = 0;
                } else {
                    res[x * size + y] = 1;
                }
            } else {
                // white tile
                if (neighbours == 2) {
                    res[x * size + y] = 1;
                } else {
                    res[x * size + y] = 0;
                }
            }
        }
    }
}

for (let i = 0; i < 50; ++i) {
    next(grid, grid2);
    next(grid2, grid);
}

let final = 0;
for (let x = 1; x < size - 1; ++x) {
    for (let y = 1; y < size - 1; ++y) {
        if (grid[x * size + y]) {
            final++;
        }
    }
}

